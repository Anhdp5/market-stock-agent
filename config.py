def _env(name: str, default: str = "") -> str:
    """
    Read an env var, preferring a base64-encoded `<NAME>_B64` variant if set.

    The AgentBase Runtime's env injection fails to start the pod when a value
    contains certain characters (e.g. '@' in email/SMTP credentials). Supplying
    such secrets base64-encoded as `<NAME>_B64` sidesteps that — this helper
    transparently decodes them, so the rest of the app is unchanged.
    """
    b64 = os.getenv(name + "_B64")
    if b64:
        try:
            return base64.b64decode(b64).decode("utf-8")
        except Exception:
            pass
    return os.getenv(name, default)

