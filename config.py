def _resolve_db_path() -> Path:
    """
    Try the default DB path. Fall back to /tmp if stale journal blocks writes.
    """
    candidate = BASE_DIR / os.getenv("DB_PATH", "data/market_intelligence.db")
    try:
        candidate.parent.mkdir(parents=True, exist_ok=True)
        conn = sqlite3.connect(str(candidate))
        conn.execute("CREATE TABLE IF NOT EXISTS _probe (id INTEGER PRIMARY KEY)")
        conn.execute("INSERT OR REPLACE INTO _probe VALUES (1)")
        conn.commit()
        conn.close()
        return candidate
    except Exception as exc:
        fallback = Path(tempfile.gettempdir()) / "market_intelligence.db"
        import logging
        logging.getLogger("config").warning(
            "DB %s blocked (%s). Using fallback: %s", candidate, exc, fallback
        )
        return fallback

